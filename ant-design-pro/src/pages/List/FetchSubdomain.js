@connect(({ rule_fetchsubdomain, loading }) => ({
  rule_fetchsubdomain,
  loading: loading.models.rule_fetchsubdomain,
}))
@Form.create()
class TableList extends PureComponent {
  state = {
    modalVisible: false,
    updateModalVisible: false,
    expandForm: false,
    selectedRows: [],
    formValues: {},
    stepFormValues: {},
  };

  columns = [
    {
      title: '子域名ID',
      dataIndex: 'id',
      // render: text => <Link to={`/profile/basic/${id}`}>{text}</Link>,
    },
    {
      title: '子域名',
      dataIndex: 'subdomain',
      render: text => <a href={`http://${text}`} target="_black">{text}</a>,
    },

    {
      title: '首页标题',
      dataIndex: 'title',
      width: 250,
      // textWrap: 'word-break',
    },
    {
      title: '扫描状态',
      dataIndex: 'status',
      filters: [
        {
          text: status[0],
          value: 0,
        },
        {
          text: status[1],
          value: 1,
        },
        {
          text: status[2],
          value: 2,
        },
        {
          text: status[3],
          value: 3,
        },
      ],
      render(val) {
        return <Badge status={statusMap[val]} text={status[val]} />;
      },
    },
    {
      title: '更新时间',
      dataIndex: 'update_time',
      sorter: true,
      render: val => <span>{moment(val).format('YYYY-MM-DD HH:mm:ss')}</span>,
    },
    {
      title: '操作',
      render: (text, record) => (
        <Fragment>
          {/* <a onClick={() => this.handleUpdateModalVisible(true, record)}>配置</a> */}
          <a onClick={() => this.deleteSubdomain(true, record)}>删除</a>
          <Divider type="vertical" />
          {/* <a onClick={() => this.updateVulnStatus(true, record, verify=2)}>忽略</a> */}
          {/* <a href="">订阅警报</a> */}
        </Fragment>
      ),
    },
  ];

  componentDidMount() {
    const { dispatch } = this.props;
    dispatch({
      type: 'rule_fetchsubdomain/fetch',
    });
  }

  handleStandardTableChange = (pagination, filtersArg, sorter) => {
    const { dispatch } = this.props;
    const { formValues } = this.state;

    const filters = Object.keys(filtersArg).reduce((obj, key) => {
      const newObj = { ...obj };
      newObj[key] = getValue(filtersArg[key]);
      return newObj;
    }, {});

    const params = {
      page: pagination.current,
      page_size: pagination.pageSize,
      ...formValues,
      ...filters,
    };
    if (sorter.field) {
      params.sorter = `${sorter.field}_${sorter.order}`;
    }

    dispatch({
      type: 'rule_fetchsubdomain/fetch',
      payload: params,
    });
  };

  handleFormReset = () => {
    const { form, dispatch } = this.props;
    form.resetFields();
    this.setState({
      formValues: {},
    });
    dispatch({
      type: 'rule_fetchsubdomain/fetch',
      payload: {},
    });
  };

  toggleForm = () => {
    const { expandForm } = this.state;
    this.setState({
      expandForm: !expandForm,
    });
  };

  handleMenuClick = e => {
    const { dispatch } = this.props;
    const { selectedRows } = this.state;

    if (selectedRows.length === 0) return;
    switch (e.key) {
      case 'remove':
        dispatch({
          type: 'rule/remove',
          payload: {
            key: selectedRows.map(row => row.key),
          },
          callback: () => {
            this.setState({
              selectedRows: [],
            });
          },
        });
        break;
      default:
        break;
    }
  };

  handleSelectRows = rows => {
    this.setState({
      selectedRows: rows,
    });
  };

  handleSearch = e => {
    e.preventDefault();

    const { dispatch, form } = this.props;

    form.validateFields((err, fieldsValue) => {
      if (err) return;

      const values = {
        ...fieldsValue,
        updatedAt: fieldsValue.updatedAt && fieldsValue.updatedAt.valueOf(),
      };

      this.setState({
        formValues: values,
      });

      dispatch({
        type: 'rule_fetchsubdomain/fetch',
        payload: values,
      });
    });
  };

  handleModalVisible = flag => {
    this.setState({
      modalVisible: !!flag,
    });
  };

  handleUpdateModalVisible = (flag, record) => {
    this.setState({
      updateModalVisible: !!flag,
      stepFormValues: record || {},
    });
  };

  deleteSubdomain = (flag, record) => {
    // this.setState({
    //   modalVisible: !!flag,
    // });
    const { dispatch } = this.props;
    const { selectedRows } = this.state;
    const { rule_fetchsubdomain: {data} } = this.props;
    // console.log("#############################");
    // console.log(selectedRows);
    // console.log(this.state);
    // console.log(this.props);
    // console.log(data.list);
    // console.log("#############################");
    let data2 = {
      'id': record.id,
      // 'verify': verify
    }
    dispatch({
      type: 'rule_fetchsubdomain/delete',
      payload: data2,
      callback: (result) => {
        // console.log("in callback function");
        // console.log(result);
          if(result.status == 200){

            data.list = data.list.filter(function(p){
                return p.id != data2.id;
            });
            message.success(result.reason);
            console.log("data.list//");
            console.log(data.list);
            this.props.rule_fetchsubdomain.data.list = data.list;
          }else{
            message.error(result.reason);
          }
      },
    });

  }


  handleAdd = fields => {
    const { dispatch } = this.props;
    const { rule_fetchsubdomain: {data} } = this.props;
    dispatch({
      type: 'rule_fetchsubdomain/add',
      payload: {
        fields,
      },
      callback: (result) => {
        console.log("in callback function");
        console.log(result);
        if (result.status == 200){
          // message.success('添加成功');
          message.success(result.reason);
          console.log("data.list//");
          console.log(data.list);
          data.list.push(result.result);
          console.log(data.list);
          this.props.rule_fetchsubdomain.data.list = data.list;
        }else{
          message.error(result.reason);
        }
      }
    });

    
    this.handleModalVisible();
  };

  handleUpdate = fields => {
    const { dispatch } = this.props;
    const { formValues } = this.state;
    dispatch({
      type: 'rule/update',
      payload: {
        query: formValues,
        body: {
          name: fields.name,
          desc: fields.desc,
          key: fields.key,
        },
      },
    });

    message.success('配置成功');
    this.handleUpdateModalVisible();
  };

  renderSimpleForm() {
    const {
      form: { getFieldDecorator },
    } = this.props;
    return (
      <Form onSubmit={this.handleSearch} layout="inline">
        <Row gutter={{ md: 8, lg: 24, xl: 48 }}>
          <Col md={6} sm={24}>
            <FormItem label="子域名">
              {getFieldDecorator('subdomain__icontains')(<Input placeholder="请输入" />)}
            </FormItem>
          </Col>
          <Col md={6} sm={24}>
            <FormItem label="标题">
              {getFieldDecorator('title__icontains')(<Input placeholder="输入标题" />)}
            </FormItem>
          </Col>
          <Col md={6} sm={24}>
            <FormItem label="域名ID">
              {getFieldDecorator('id_domain')(<Input placeholder="请输入" />)}
            </FormItem>
          </Col>
          <Col md={6} sm={24}>
            <span className={styles.submitButtons}>
              <Button type="primary" htmlType="submit">
                查询
              </Button>
              <Button style={{ marginLeft: 8 }} onClick={this.handleFormReset}>
                重置
              </Button>
              {/* <a style={{ marginLeft: 8 }} onClick={this.toggleForm}>
                展开 <Icon type="down" />
              </a> */}
            </span>
          </Col>
        </Row>
      </Form>
    );
  }


  renderForm() {
    // const { expandForm } = this.state;
    return this.renderSimpleForm();
  }

  render() {
    const {
      rule_fetchsubdomain: { data },
      loading,
    } = this.props;
    const { selectedRows, modalVisible, updateModalVisible, stepFormValues } = this.state;
    const menu = (
      <Menu onClick={this.handleMenuClick} selectedKeys={[]}>
        <Menu.Item key="remove">删除</Menu.Item>
        <Menu.Item key="approval">批量审批</Menu.Item>
      </Menu>
    );

    const parentMethods = {
      handleAdd: this.handleAdd,
      handleModalVisible: this.handleModalVisible,
    };
    const updateMethods = {
      handleUpdateModalVisible: this.handleUpdateModalVisible,
      handleUpdate: this.handleUpdate,
    };
    return (
      <PageHeaderWrapper title="查询表格">
        <Card bordered={false}>
          <div className={styles.tableList}>
            <div className={styles.tableListForm}>{this.renderForm()}</div>
            <div className={styles.tableListOperator}>
              <Button icon="plus" type="primary" onClick={() => this.handleModalVisible(true)}>
                新建
              </Button>
              {selectedRows.length > 0 && (
                <span>
                  <Button>批量操作</Button>
                  <Dropdown overlay={menu}>
                    <Button>
                      更多操作 <Icon type="down" />
                    </Button>
                  </Dropdown>
                </span>
              )}
            </div>
            <StandardTable
              selectedRows={selectedRows}
              loading={loading}
              data={data}
              columns={this.columns}
              scroll={{x : true}}
              expandedRowRender={record =>
                  <div style={{ margin: 0, padding: 0 }}>
                    <span style={{ margin: 0, whiteSpace: 'pre-wrap' }}>请求头:{record.headers_string}</span>
                    <Divider />
                    <span style={{ margin: 0, whiteSpace: 'pre-wrap' }}>指纹:{record.cmstype}</span>
                    <Divider />
                    <span style={{ margin: 0 }}>更新时间: {record.create_time} 
                    {/* <Divider type="vertical" /> 漏洞更新时间: {record.modified}  */}
                    <Divider type="vertical" /> 来源: {record.id_domain}</span>
                  </div>
                }
              onSelectRow={this.handleSelectRows}
              onChange={this.handleStandardTableChange}
            />
          </div>
        </Card>
        <CreateForm {...parentMethods} modalVisible={modalVisible} />
        {stepFormValues && Object.keys(stepFormValues).length ? (
          <UpdateForm
            {...updateMethods}
            updateModalVisible={updateModalVisible}
            values={stepFormValues}
          />
        ) : null}
      </PageHeaderWrapper>
    );
  }
}

export default TableList;
